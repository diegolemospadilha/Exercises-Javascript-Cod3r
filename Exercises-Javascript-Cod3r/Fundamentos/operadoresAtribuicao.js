// Aula 37 - Operadores de atribuição

const a = 10; // const a recebe 10
let b = 5;

console.log("a = ",a);
console.log("b = ",b);
b += a; // b = b+a ;
console.log("(b += a) = ",b);

b -= a; // b= b - a;
console.log("(b -= a) = ",b);

b *= a; // b = b*a
console.log("(b *= a) = ",b);

b /= a; // b = b/a;
console.log("(b /= a) = ",b);

b %= a;
console.log("(b %= a) = ",b);
